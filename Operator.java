public class Operator {
    public static void main(String[] args) {
        int m = 40, n = 20;
        int A;
        int O;
        int XOR;
        A = m & n;
        O = m | n;
        XOR = m ^ n;
        System.out.println("AND operator value = " + A);
        System.out.println("XOR operator value = " + XOR);
        System.out.println("OR operator value = " + O);
    }
}

